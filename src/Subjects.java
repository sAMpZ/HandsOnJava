
public class Subjects {
	
	//subject name
	private String subject;
	
	// total marks for each subject
	private int marks;

	
	public String getSubject() {
		return subject;
		
	}

	public void setSubject(String subject) {
		this.subject = subject;
		
	}

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}
	
	
	
	
}
